<template><h1 id="create-a-discord-bot" tabindex="-1"><a class="header-anchor" href="#create-a-discord-bot" aria-hidden="true">#</a> Create a Discord bot</h1>
<p>Creating a Discord bot is a very important step to create your own ModMail. This will allow you to change its name and avatar to your desire.</p>
<ul>
<li>Go to <a href="https://discord.com/developers/applications" target="_blank" rel="noopener noreferrer">Discord's Developer Portal<OutboundLink/></a> and login with your Discord account.</li>
<li>Click over the <strong>New Application</strong> button.</li>
</ul>
<p><img src="/images/Dc_NewApp.png" alt=""></p>
<ul>
<li>Enter a name for your app (ideally the name you want for your bot) and create the App.</li>
<li>Add an avatar if you want to and head to the <strong>Bot</strong> tab. Once there, click over <strong>Add Bot</strong>.</li>
</ul>
<p><img src="/images/Dc_AddBot.png" alt=""></p>
<ul>
<li>Disable the <strong>Public Bot</strong> toggle so that nobody but you can add the bot to their servers. You also need to enable <strong>Privileged Intents</strong>.</li>
</ul>
<p><img src="/images/Dc_Intents.png" alt=""></p>
<ul>
<li>Go to the <strong>OAuth2</strong> tab and select <u><em>Bot</em></u> in the first checkbox list. Then select <u><em>Administrator</em></u> in the second one. Copy the link and open it in your browser so that you can add the bot to your Discord server.</li>
</ul>
<p><img src="/images/Dc_OAuth.png" alt=""></p>
<br/>
<div class="custom-container tip"><p class="custom-container-title">Congratulations!</p>
<p>You have successfully created and added your bot and it should now appear in your server.</p>
</div>
